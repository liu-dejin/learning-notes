# 赋值运算符
a = 10
print(a)
 
# 多个变量赋同一个值
b = c = 20
print(b)
print(c)
 
# 多个变量同时赋值
d,e,f = 1,2,3
print(d)
print(e)
print(f)
 
# 复合赋值运算符
a = 1
 
a = a + 4
print(a)
 
a += 2
print(a)
 
a -= 2
print(a)
 
a *= 2
print(a)
 
a /= 2
print(a)
 
a //= 2
print(a)
 
a %= 2
print(a)
 
a **= 2
print(a)

# python支持多个变量同时赋不同的值，但是变量间和值之间都要用逗号隔开